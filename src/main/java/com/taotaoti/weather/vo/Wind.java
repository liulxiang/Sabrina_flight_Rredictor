package com.taotaoti.weather.vo;

public class Wind {
	String direction;
	String directionIsVariable;
	String speedKnots;
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public String getDirectionIsVariable() {
		return directionIsVariable;
	}
	public void setDirectionIsVariable(String directionIsVariable) {
		this.directionIsVariable = directionIsVariable;
	}
	public String getSpeedKnots() {
		return speedKnots;
	}
	public void setSpeedKnots(String speedKnots) {
		this.speedKnots = speedKnots;
	}

}
